package chap09;

public class MathExample1 {

	public static void main(String[] args) {
		System.out.println("2의3승은 " + Math.pow(2, 3));
		System.out.println("16의 제곱근은 " + Math.sqrt(16));
		System.out.println("4와7 중 큰 수는? " + Math.max(4, 7));
		System.out.println("4와7 중 작은 수는? " + Math.min(4, 7));

	}

}
