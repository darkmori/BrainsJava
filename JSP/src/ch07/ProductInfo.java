public class ProductInfo {
	private String name;
	private int value;
	
	public ProductInfo() {
		
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	
	public void setValue(int price) {
		value = price;
	}

	public int getValue() {
		return value;
	}

}
